// 81칸 배열 생성 유틸리티
export const buildCellsFromPackage = (pkg: { core: string; themes: { name: string; items: string[] }[] }): string[] => {
  const cells = Array(81).fill("");
  cells[40] = pkg.core;

  const centerSurr = [30, 31, 32, 39, 41, 48, 49, 50];
  const outerBlocks = [0, 1, 2, 3, 5, 6, 7, 8];

  pkg.themes.forEach((theme, ti) => {
    cells[centerSurr[ti]] = theme.name;

    const bi = outerBlocks[ti];
    const br = Math.floor(bi / 3),
      bc = bi % 3;
    const sr = br * 3,
      sc = bc * 3;
    const centerIdx = (sr + 1) * 9 + (sc + 1);
    cells[centerIdx] = theme.name;

    const surrounding = [
      sr * 9 + sc, sr * 9 + sc + 1, sr * 9 + sc + 2,
      (sr + 1) * 9 + sc, (sr + 1) * 9 + sc + 2,
      (sr + 2) * 9 + sc, (sr + 2) * 9 + sc + 1, (sr + 2) * 9 + sc + 2,
    ];
    theme.items.forEach((item, ii) => {
      if (ii < surrounding.length) cells[surrounding[ii]] = item;
    });
  });

  return cells;
};
